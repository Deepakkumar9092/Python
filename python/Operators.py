# Relational Operator
print("Relational Operator")
a=10
b=20
print(a>b)#False    
print(a<b)#True
print(a==b)#False
print(a!=b)#True
print(a>=b)#False   
print(a<=b)#True

# Assignment Operator
print("Assignment Operator")
a=10
