print(a)
a-=10 #a=a-10   
print(a)
a*=10 #a=a*10
print(a)
a/=10
print(a)
a%=10 #a=a%10
print(a)
a**=10 #a=a**10
print(a)
a//=10 #a=a//10
print(a)


# Logical Operator
print("Logical Operator")
a=True
b=False
print(a and b)#False    
print(a or b)#True
print(not a)#False

# Bitwise Operator
print("Bitwise Operator")
a=10
b=4
print(a&b)#8    
print(a|b)#14
print(a^b)#12
print(~a)#-11
print(a<<1)#20
